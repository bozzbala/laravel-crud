// Функция для формирования строки во множественной форме на русском языке
export default function ruPluralize(number, strings) {
    // Проверяем, есть ли у нас четыре формы (0, 1, 2-4, множественная)
    if (strings.length === 4) {
        // Если число равно 0, возвращаем форму для нуля
        if (number === 0) {
            return strings[0];
        }

        // Удаляем первый элемент (форму для 0), так как она нам больше не нужна
        strings.shift();
    }

    // Приводим число к диапазону от 0 до 99
    number = number % 100;

    // Если число больше 20, приводим его к диапазону от 0 до 9
    if (number > 20) {
        number = number % 10;
    }

    // Если число равно 1, возвращаем форму для единственного числа
    if (number === 1) {
        return strings[0];
    }

    // Если число больше 1 и меньше 5, возвращаем форму для 2-4
    if (number > 1 && number < 5) {
        return strings[1];
    }

    // Возвращаем форму для множественного числа (5 и более)
    return strings[2];
}
